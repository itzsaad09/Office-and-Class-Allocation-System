    private void IDFieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_IDFieldKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()==KeyEvent.VK_ENTER) {
            String ID = IDField.getText();
            try {
                String query = "SELECT * FROM `instructor` WHERE `ID` = ?";
                pst = connect.prepareStatement(query);
                pst.setString(1, ID);
                result = pst.executeQuery();
                if (result.next()) {
                    FirstNameField.setText(result.getString(2));
                    LastNameField.setText(result.getString(3));
                    CNICField.setText(result.getString(4));
                    PhoneNoField.setText(result.getString(5));
                    Gender.setSelectedItem(result.getString(6));
                    Office.setSelectedItem(result.getString(7));
                    if(result.getString(8).equals("Active")) {
                        Active.setSelected(true);
                        Inactive.setSelected(false);
                    } else if(result.getString(8).equals("In-Active")) {
                        Active.setSelected(false);
                        Inactive.setSelected(true);
                    }
                    Department.setSelectedItem(result.getString(9));
                    EmailField.setText(result.getString(10));
                    PasswordField.setText(result.getString(11));
                } else {
                    IDNotFound idnotfound = new IDNotFound();
                    idnotfound.setVisible(true);
                    new Timer().schedule(new TimerTask() {
                        public void run() {
                            idnotfound.dispose();
                        }
                    }, 1000);
                    
                    FirstNameField.setText("");
                    LastNameField.setText("");
                    CNICField.setText("");
                    PhoneNoField.setText("");
                    Gender.setSelectedItem("Select Gender");
                    Office.setSelectedItem("Select Office");
                    Active.setSelected(false);
                    Inactive.setSelected(false);
                    Department.setSelectedItem("Select Department");
                    EmailField.setText("");
                    PasswordField.setText("");
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null,ex);
            }
        }
    }//GEN-LAST:event_IDFieldKeyPressed

